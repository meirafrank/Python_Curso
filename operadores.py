# > OPERAÇÕES MATEMÁTICAS (ARITMÉTICAS)

numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2) # considera somente a parte inteira, desconsidera decimal
print( 20 // 3)  # considera somente a parte inteira, desconsidera decimal
print(20 % 3)
print(2 ** 3)

